package util

import org.scalatest.{FlatSpec, Matchers}

class PolynomialSpec extends FlatSpec with Matchers {

  "Polynomial" should "give its degree" in {
    Polynomial(Nil).degree shouldBe 0
    Polynomial(List(1)).degree shouldBe 0
    Polynomial(List(1,0,0)).degree shouldBe 2
  }

  it should "have a textual representation" in {
    Polynomial(Nil).toString shouldBe "0"
    Polynomial(List(1)) shouldBe "1.0 * x^0"
    Polynomial(List(1,0,0)).toString shouldBe "1.0 * x^2"
  }
}
